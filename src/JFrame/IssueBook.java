    //veritabanından kitap bilgilerini alan ve kitap bilgileri panelinde gösteren metod
    public void getBookDetails() {
        int bookId = Integer.parseInt(txt_bookID.getText());

        try {
            Connection con = DBConnection.getConnection();
            PreparedStatement pst = con.prepareStatement("select * from book_details where book_id = ?");
            pst.setInt(1, bookId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                lbl_bookId.setText(rs.getString("book_id")); //DBdeki column adı
                lbl_bookName.setText(rs.getString("book_name"));
                lbl_author.setText(rs.getString("author"));
                lbl_quantity.setText(rs.getString("quantity"));
            } else {
                //Veritabanında böyle bir kayıt yoksa
                lbl_bookError.setText("Geçersiz ID");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    //insert issue book details to database
    public boolean issueBook() {
        boolean isIssued = false;
        int bookId = Integer.parseInt(txt_bookID.getText());
        String bookName = lbl_bookName.getText();
        String authorName = lbl_author.getText();
        String username = txt_username.getText();

        try {
            Connection con = DBConnection.getConnection();
            String sql = "insert into issued_books(Book_ID, Book_Name, Author_Name, Username) values (?,?,?,?)";

            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, bookId);
            pst.setString(2, bookName);
            pst.setString(3, authorName);
            pst.setString(4, username);

            int rowCount = pst.executeUpdate();
            if (rowCount > 0) {
                isIssued = true;
            } else {
                isIssued = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return isIssued;
    }

    //ödünç alma işleminden sonra kitap sayısını günceller
    public void updateBookCount() {
        int bookId = Integer.parseInt(txt_bookID.getText());

        try {
            Connection con = DBConnection.getConnection();
            String sql = "update book_details set quantity = quantity - 1 where Book_ID = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, bookId);
            int rowCount = pst.executeUpdate();

            if (rowCount > 0) {
                JOptionPane.showMessageDialog(this, "Kitap adedi güncellendi.");
                int initialCount = Integer.parseInt(lbl_quantity.getText());
                lbl_quantity.setText(Integer.toString(initialCount - 1));
            } else {
                JOptionPane.showMessageDialog(this, "Kitap adedi güncellenemedi.");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
