    private void issueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueButtonActionPerformed
        if (lbl_quantity.getText().equals("0")) {
            JOptionPane.showMessageDialog(this, "Bu kitap kullanılamıyor.");
        } else {
            if (isAlreadyIssued() == false) {

                  if (issueBook() == true) {
                    JOptionPane.showMessageDialog(this, "Kitap alındı.");
                    updateBookCount();
                } else {
                    JOptionPane.showMessageDialog(this, "Kitap alınamadı.");
                }

          } else {
                JOptionPane.showMessageDialog(this, "Bu kitap bu kullanıcıda zaten var.");
          }
          }
    }//GEN-LAST:event_issueButtonActionPerformed

